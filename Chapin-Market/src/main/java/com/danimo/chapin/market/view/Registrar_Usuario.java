    private void combo_tipo_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_tipo_usuarioActionPerformed
        // TODO add your handling code here:
        if(combo_tipo_usuario.getSelectedItem().toString().equals("Administrador")){
            no_caja_txt.setEnabled(false);
            combo_sucursal.setEnabled(false);
        }else if(combo_tipo_usuario.getSelectedItem().toString().equals("Cajero")) {
            no_caja_txt.setEnabled(true);
            combo_sucursal.setEnabled(true);
        }else if(combo_tipo_usuario.getSelectedItem().toString().equals("Bodeguero")) {
            no_caja_txt.setEnabled(false);
            combo_sucursal.setEnabled(true);
        }else if(combo_tipo_usuario.getSelectedItem().toString().equals("Inventarista")) {
            no_caja_txt.setEnabled(false);
            combo_sucursal.setEnabled(true);
        }
    }//GEN-LAST:event_combo_tipo_usuarioActionPerformed

    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO Guardare un nuevo empleado
        String nombre = name_txt.getText();
        String apellido = last_name_txt.getText();
        String username = username_txt.getText();
        String password = password_txt.getText();
        if(no_caja_txt.getText().equals("")){
            no_caja_txt.setText("0");
        }
        int no_caja = Integer.parseInt(no_caja_txt.getText());
        int sucursal = combo_sucursal.getSelectedIndex()+1;
        if(combo_tipo_usuario.getSelectedItem().toString().equals("Administrador")){
            no_caja = 0;
            sucursal = 1;
        }
        int tipo_usuario = combo_tipo_usuario.getSelectedIndex()+1;
        Empleado empleado = new Empleado(nombre, apellido, username, password, no_caja, Sucursal.getSucursal(sucursal), Rol.getRol(tipo_usuario));
        new EmpleadoDaoImpl().insertar(empleado);
        System.out.println(empleado.toString());
        this.clearAll();


    }//GEN-LAST:event_button_saveActionPerformed

